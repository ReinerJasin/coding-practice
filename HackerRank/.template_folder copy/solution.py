def pickingNumbers(a):
    # Write your code here
    max_counter = 0
    
    for number in a:
        filtered = list(filter(lambda x: (x == number or x == number+1), a))
        
        if len(filtered) > max_counter:
            max_counter = len(filtered)
    
    return max_counter

